// WAP to calculate area of rectangle from std inputs
#include <stdio.h>

int main(void) {
	int length,breadth;
	printf("What is the length of the Rectangle \n");
	scanf("%d",&length);
	printf("What is the breadth of the Rectangle \n");
	scanf("%d",&breadth);
	printf("Area of a Rectangle is %d",length*breadth);
	return 0;
}

